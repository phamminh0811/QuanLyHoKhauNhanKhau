package model;

public class DinhChinh {
    private String maHo;
    private String thongTinThayDoi;
    private String thayDoiTu;
    private String thayDoiThanh;
    private String ngayThayDoi;
    private String nguoiThayDoi;
    public DinhChinh(String maHo, String thongTinThayDoi, String thayDoiTu, String thayDoiThanh, String ngayThayDoi, String nguoiThayDoi) {
        this.maHo = maHo;
        this.thongTinThayDoi = thongTinThayDoi;
        this.thayDoiTu = thayDoiTu;
        this.thayDoiThanh = thayDoiThanh;
        this.ngayThayDoi = ngayThayDoi;
        this.nguoiThayDoi = nguoiThayDoi;
    }
}
